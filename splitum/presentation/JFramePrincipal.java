    private void jButtonLoginOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginOKActionPerformed
        String username = jTextFieldLoginUsername.getText();
        String password = new String(jPasswordFieldLogin.getPassword());
        if(username.equals("") || password.equals("")) {
            jLabelLoginErro.setText(ERRO_DADOS_INCOMPLETOS);
            jLabelLoginErro.setForeground(Color.red);
        }
        else {
            try {
                splitUM.login(username, password);
                
                displayUserIcon();
                jPanelContaCorrente.init();
                cardLayout.show(jPanelPrincipal, "card_cc"); 
            }
            catch (MoradorInexistenteException ex) {
                
                jLabelLoginErro.setText("ERRO: " + ex.getMessage());
                jLabelLoginErro.setForeground(Color.red);
                jTextFieldUsername.setText("");
            }
            catch (PasswordInvalidaException ex) {
                jLabelLoginErro.setText("ERRO: " + ex.getMessage());
                jLabelLoginErro.setForeground(Color.red);
                jTextFieldUsername.setText("");
            }
        }
    }//GEN-LAST:event_jButtonLoginOKActionPerformed

