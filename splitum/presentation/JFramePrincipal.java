    protected void pushVoltar(JPanel p, String card, Boolean mostrarAnterior) {
        voltarPanels.push(p);
        voltarCards.push(card);
        voltarMostrarAnterior.push(mostrarAnterior);
        mostrarVoltar();
    }
    
    protected void popVoltar() {
        JPanel voltarPanel = voltarPanels.pop();
        CardLayout voltarCL = (CardLayout) voltarPanel.getLayout();
        voltarCL.show(voltarPanel, voltarCards.pop());
        if(!voltarMostrarAnterior.pop()) {
            esconderVoltar();
        } else {
            mostrarVoltar();
        }
    }
    
    protected void clearVoltar() {
        while(!voltarPanels.empty()) voltarPanels.pop();
        while(!voltarCards.empty()) voltarCards.pop();
        while(!voltarMostrarAnterior.empty()) voltarMostrarAnterior.pop();
        esconderVoltar();
    }
    
    protected void mostrarVoltar() {
        jLabelVoltar.setForeground(Color.BLACK);
    }
    
    protected void esconderVoltar() {
        jLabelVoltar.setForeground(Color.WHITE);
    }
    
